# membuat program luas persegi panjang
p <- 2
l <- 4
rectangle_area <- p * l
cat("Luas Persegi panjang :", rectangle_area, "\n")

# membuat program luas segitiga
a <- 3
t <- 4
triangle_area <- (1 / 2) * a * t
cat("Luas Segitiga :", triangle_area, "\n")

# membuat program volume balok
