a <- 3
b <- 4

# No. 1 : Operasi Aritmatika
# Penjumlahan
cat("Hasil penjumlahan", a, "+", b, ":", a + b, "\n")
# Pengurangan
cat("Hasil pengurangan", a, "-", b, ":", a - b, "\n")
# Perkalian
cat("Hasil perkalian", a, "*", b, ":", a * b, "\n")
# Pembagian
cat("Hasil pembagian", a, "/", b, ":", a / b, "\n")
# Sisa Hasil Bagi
cat("Hasil sisa bagi", a, "%%", b, ":", a %% b, "\n")
# Pangkat
cat("Hasil pangkat", a, "^", b, ":", a^b, "\n")
